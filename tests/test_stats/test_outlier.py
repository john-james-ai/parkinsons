import inspect
from datetime import datetime
import pytest
import logging
import numpy as np
from parkinsons.statistics.outlier import Outlier


# ------------------------------------------------------------------------------------------------ #
logger = logging.getLogger(__name__)
# ------------------------------------------------------------------------------------------------ #
double_line = f"\n{100 * '='}"
single_line = f"\n{100 * '-'}"


@pytest.mark.outlier
class TestOutlier:  # pragma: no cover
    # ============================================================================================ #
    def test_outlier(self, caplog):
        start = datetime.now()
        logger.info(
            "\n\nStarted {} {} at {} on {}".format(
                self.__class__.__name__,
                inspect.stack()[0][3],
                start.strftime("%I:%M:%S %p"),
                start.strftime("%m/%d/%Y"),
            )
        )
        logger.info(double_line)
        # ---------------------------------------------------------------------------------------- #
        a = np.random.randint(1, 200, 200)
        med = np.median(a)
        centered = np.abs(a - med)
        median = np.median(centered)
        mad = median * 1.4826
        b = a[np.abs((a - median) / mad) < 3]
        outlier = Outlier()
        c = outlier.univariate(a)
        assert b.all() == c.all()

        # ---------------------------------------------------------------------------------------- #
        end = datetime.now()
        duration = round((end - start).total_seconds(), 1)

        logger.info(
            "\nCompleted {} {} in {} seconds at {} on {}".format(
                self.__class__.__name__,
                inspect.stack()[0][3],
                duration,
                end.strftime("%I:%M:%S %p"),
                end.strftime("%m/%d/%Y"),
            )
        )
        logger.info(single_line)
